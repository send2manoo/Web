package com.qlc.fieldsense.addressConverter;

/**
 *
 * @author Ramesh
 * @date 20-08-2014
 */
public class Location {

    private String lat;
    private String lng;

    /**
     *
     * @return
     */
    public String getLat() {
        return lat;
    }

    /**
     *
     * @param lat
     */
    public void setLat(String lat) {
        this.lat = lat;
    }

    /**
     *
     * @return
     */
    public String getLng() {
        return lng;
    }

    /**
     *
     * @param lng
     */
    public void setLng(String lng) {
        this.lng = lng;
    }
}
